#Написать функцию принимающая на вход неопределенным количеством аргументов и именованный аргумент func_type.
# В зависимости от func_type вернуть минимальное либо максимальное значение. Написать программу в виде трех функций.
def get_min(*args):
#Возвращает минимальное значение из переданных аргументов.
    return min(args)

def get_max(*args):
#Возвращает максимальное значение из переданных аргументов.

    return max(args)

def process_args(func_type="min", *args):
    # Обрабатывает переданные аргументы и возвращает минимальное или максимальное значение.
    #
    # :param func_type: Тип операции ("min" для минимального, "max" для максимального значения).
    # :param args: Неопределённое количество позиционных аргументов.
    # :return: Результат минимального или максимального значения.

    if func_type == "min":
        return get_min(*args)
    elif func_type == "max":
        return get_max(*args)
    else:
        raise ValueError("func_type должен быть 'min' или 'max'.")

