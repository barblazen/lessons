#Клетки на шахматной доске идентифицируются буквой и цифрой.
# Буква определяет положение клетки по горизонтали, а цифра – по вертикали,
# Ваша программа должна запрашивать у пользователя координаты клетки.
# Используйте условное выражение для определения того, с какой клетки – белой или черной – начинается столбец.
# Затем при помощи обычной арифметики необходимо определить цвет конкретной клетки.
# Например, если пользователь ввел a1, программа должна определить, что клетка с этими координатами черная.
# Если d5 – белая. Проверку на ошибочность ввода координат клетки выполнять не нужно

coordinates = input("Введите координаты клетки : ").lower()

column = coordinates[0]  # Буква
row = int(coordinates[1])  # Цифра

# Преобразуем букву в число (a=1, b=2, ..., h=8)
column_number = ord(column) - ord('a') + 1
#ord(column) возвращает ASCII-код буквы (a → 97, b → 98, ...).
# ord('a') — ASCII-код a (97), вычитая его, мы получаем 0 для a, 1 для b, и т. д.
# Добавляем +1, чтобы a стало 1, b — 2 и так далее.

# Суммируем номер строки и столбца
total = column_number + row

# Определяем цвет клетки
if total % 2 == 0:
    print(f"Клетка {coordinates} черная.")
else:
    print(f"Клетка {coordinates} белая.")
