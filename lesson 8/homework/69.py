# В зоопарке цена входного билета зависит от возраста посетителя. Дети до двух лет допускаются бесплатно.
# Дети в возрасте от трех до 12 лет могут посещать зоопарк за $14,00.
# Пенсионерам старше 65 лет вход обойдется в $18,00, а обычный взрослый билет стоит $23,00.
#  Напишите программу, которая будет запрашивать возраст всех посетителей в группе (по одному за раз)
# и выводить общую цену билетов для посещения зоопарка этой группой.
# В качестве индикатора окончания ввода можно по традиции использовать пустую строку.
# Общую цену билетов стоит выводить в формате с двумя знаками после запятой.

# Цены билетов
PRICE_CHILD = 0.00  # Для детей до 2 лет
PRICE_KID = 14.00  # Для детей от 3 до 12 лет
PRICE_ADULT = 23.00  # Для взрослых от 13 до 65 лет
PRICE_SENIOR = 18.00  # Для пенсионеров старше 65 лет

# Инициализация общей стоимости
total_price = 0.00

while True:
    # Запрашиваем возраст посетителя
    age_input = input("Введите возраст посетителя : ")

    # Прекращаем ввод, если строка пустая
    if age_input == "":
        break

    # Преобразуем введенный возраст в число
    age = int(age_input)

    # Определяем стоимость билета для данного возраста
    if age < 3:
        price = PRICE_CHILD
    elif 3 <= age <= 12:
        price = PRICE_KID
    elif age > 65:
        price = PRICE_SENIOR
    else:
        price = PRICE_ADULT

    # Добавляем стоимость к общей цене
    total_price += price

# Выводим общую стоимость с форматированием до двух знаков
print(f"Общая стоимость билетов: ${total_price:.2f}")
